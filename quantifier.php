[els]{1,3} : 1개,2개 또는 3개를 만족하는 것중에 e,l,s 중에 하나가 해당되어 선택됨
[a-z]{3,} : 3이상 무한대 숫자이며, a부터 z까지 공백/대문자는 빠진 
		  소문자 알파벳들만 해당되어 선택됨

[a-z]{0,2} : 0부터 시작하는 건 없음~!!
AB{0,}A : ????
AB?A : ???
AB{0,1}A : A가 0이거나 1이거나??? 


Lazy(게으른 수량자) : *, + {n,}       나올수 있는 최대 방법
Greedy Quantify(탐욕적 수량자) : *?, +?, {n}?  ~최소방법

- Lazy 예시 테스트:
