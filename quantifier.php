1. 수량자(Quantifier)
regex101.com  (사이트에서 테스트 해보기 참고)
/ * / 이런식으로 // 안에 문자 만든다.

* : 앞에 있는 문자가 0개 이상 >> 없거나, 있거나, 많거나
+ : 앞에 있는 문자가 1개 이상 >> 있거나, 많거나
